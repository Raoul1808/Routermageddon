package mew;

public enum RouterEventType
{
    HundredBosses, // Spawns 100 boss enemies
    PowerOut, // Removes all sources of power (and nodes)
    Pacifism, // Removes all turrets
    LuckyEnding, // Removes routers surrounding the source router
    Eksuplosion, // Makes multiple thorium reactors appear around the source router, all fed 1 piece of thorium
    OuiOuiBaguette, // Changes the game's language to french
    BulletSpiral, // Bullets spiraling around the source router
}
